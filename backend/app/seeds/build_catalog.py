# ---------------------------------------------------------------------------
# Aggregation
# ---------------------------------------------------------------------------

ALL_SONGS: list[dict[str, Any]] = (
    LOFI + CINEMATIC + AMBIENT + NATURE + JAZZ + CLASSICAL + SYNTHWAVE
)


def build_payload() -> list[dict[str, Any]]:
    """Return the validated song catalog with audio file assignments.
    Only songs whose genre has audio files are included.
    """
    audio_pool: dict[str, list[str]] = {
        g: get_audio_files(g) for g in {s["genre"] for s in ALL_SONGS}
    }
    counters: dict[str, int] = {}
    payload: list[dict[str, Any]] = []
    for song in ALL_SONGS:
        genre = song["genre"]
        files = audio_pool.get(genre, [])
        if not files:
            continue  # skip genres without audio files
        idx = counters.get(genre, 0) % len(files)
        entry = dict(song)
        entry["audio_path"] = f"{genre}/{files[idx]}"
        counters[genre] = idx + 1
        payload.append(entry)
    return payload


def main() -> int:
    output_path = Config.SEEDS_DIR / "songs.json"
    output_path.parent.mkdir(parents=True, exist_ok=True)
    payload = build_payload()
    output_path.write_text(json.dumps(payload, indent=2, ensure_ascii=False))

    by_genre: dict[str, int] = {}
    for entry in payload:
        by_genre[entry["genre"]] = by_genre.get(entry["genre"], 0) + 1
    print(f"Wrote {len(payload)} songs to {output_path}")
    for genre, count in sorted(by_genre.items()):
        print(f"  {genre:<10} {count}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
