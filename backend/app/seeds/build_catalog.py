from app.config import Config


def _track(
    title: str,
    artist: str,
    genre: str,
    moods: list[str],
    bpm: int | None,
    external_url: str,
    cover_url: str | None = None,
    audio_path: str | None = None,
) -> dict[str, Any]:
    return {
        "title": title,
        "artist": artist,
        "genre": genre,
        "moods": moods,
        "bpm": bpm,
        "external_url": external_url,
        "cover_url": cover_url,
        "audio_path": audio_path,
    }


def get_audio_files(genre: str) -> list[str]:
    genre_dir = Config.AUDIO_DIR / genre
    if not genre_dir.exists():
        return []
    return sorted(
        f.name
        for f in genre_dir.iterdir()
        if f.suffix in (".mp3", ".wav") and f.is_file()
    )


